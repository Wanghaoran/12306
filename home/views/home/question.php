                    &nbsp;&nbsp;&nbsp;&nbsp; 如果从乘车站至目的站没有直接到达的列车时，旅客购买从乘车站到换乘站的车票时，可以同时购买从换乘站至目的站的联程票。<br>
                    &nbsp;&nbsp;&nbsp;&nbsp; 往返票是指从乘车站同时购买往程（从乘车站去往目的站）和返程（从目的站返回乘车站）车票。<br>
                </div>
