                    <h3 class="panel-title">2. 什么是直达票、通票？</h3>
                </div>
                <div class="panel-body">
                    &nbsp;&nbsp;&nbsp;&nbsp; 直达票是指从票面发站至到站有明确车次和席位、且不需中转的车票。直达票当日当次有效，中途下车失效。<br>
                    &nbsp;&nbsp;&nbsp;&nbsp; 通票是指从票面发站至终到站需中转的车票。票面载有从发站至中转站的车次和席别，在中转站换乘时，需在车站售票窗口办理中转签证手续。通票按乘车里程计算有效期，旅客持票应在有效期截止前乘车至到站。<br>
                </div>
